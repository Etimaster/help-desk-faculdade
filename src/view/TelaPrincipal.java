    public void setUsuarioLogado(Usuario usuarioLogado) {
        this.usuarioLogado = usuarioLogado;
        menuChamdos.setEnabled(true);
        menuUsuario.setEnabled(true);
        if(usuarioLogado instanceof Atendente){
            menuEquipe.setEnabled(true);
            menuCriarUsuario.setEnabled(true);
            menuCriarAtendente.setEnabled(true);
            menuChamadoSemAtendentes.setEnabled(true);
            menuListarUsuario.setEnabled(true);
            menuChamadoTodos.setEnabled(true);
        }
    }
    
    void showPainel(String panel) {
        CardLayout cl = (CardLayout) painelPrincipal.getLayout();
        cl.show(painelPrincipal, panel);
        setInfo("");
    }
    
